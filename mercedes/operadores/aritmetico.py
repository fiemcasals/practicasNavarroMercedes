
a = 10
b = 3

# Sumar
suma = a + b
print("Suma:", suma)

# Restar
resta = a - b
print("Resta:", resta)

# Multiplicar
multiplicacion = a * b
print("Multiplicación:", multiplicacion)

# Dividir
division = a / b
print("División:", division)

# División entera
division_entera = a // b
print("División entera:", division_entera)


t=2
l=3
# Módulo (resto de la division)
modulo = t % l 
print("Módulo:", modulo)
 
#Exponenciación
exponenciacion = t ** l
print("Exponenciación:", exponenciacion)

