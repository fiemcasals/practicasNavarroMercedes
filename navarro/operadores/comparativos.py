

a = 5
b = 5

# igual a
igual = a == b 
print(f"El resultado de {a} == {b} es: {igual}")

"""3 == 5 -> False
4 == 4 -> True """

# distinto
distinto = a != b
print(f"El resultado de {a} != {b} es: {distinto}")

"""3 != 5 -> True
4 != 4 -> False """

# mayor que
mayor = a > b
print(f"El resultado de {a} > {b} es: {mayor}")

"""3 > 5 -> False
4 > 4 -> False"""

# menor que
menor = a < b
print(f"El resultado de {a} < {b} es: {menor}")

"""3 < 5 -> True
4 < 4 -> False"""

# mayor o igual que
mayor_igual = a >= b
print(f"El resultado de {a} >= {b} es: {mayor_igual}")

"""3 >= 5 -> False
4 >= 4 -> True"""

# menor o igual que
menor_igual = a <= b
print(f"El resultado de {a} <= {b} es: {menor_igual}")

"""3 <= 5 -> True
4 <= 4 -> True"""

